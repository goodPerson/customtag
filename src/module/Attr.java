package module;

public class Attr {

	private String attr_id;                       //属性标识
	private String attr_name;                     //属性名称
	private String attr_type;                     //属性类型
	private String attr_unit;                     //属性单位
	private String attr_table;                    //属性码表
	private String attr_is_tab;                   //属性是否码值
	public String getAttr_is_tab() {
		return attr_is_tab;
	}
	public void setAttr_is_tab(String attr_is_tab) {
		this.attr_is_tab = attr_is_tab;
	}
	public String getAttr_id() {
		return attr_id;
	}
	public void setAttr_id(String attr_id) {
		this.attr_id = attr_id;
	}
	public String getAttr_name() {
		return attr_name;
	}
	public void setAttr_name(String attr_name) {
		this.attr_name = attr_name;
	}
	public String getAttr_type() {
		return attr_type;
	}
	public void setAttr_type(String attr_type) {
		this.attr_type = attr_type;
	}
	public String getAttr_unit() {
		return attr_unit;
	}
	public void setAttr_unit(String attr_unit) {
		this.attr_unit = attr_unit;
	}
	public String getAttr_table() {
		return attr_table;
	}
	public void setAttr_table(String attr_table) {
		this.attr_table = attr_table;
	}
	
	
	
	
}
